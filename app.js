// SERVICE WORKER FOR OFFLINE PWA & AUTO-REFRESH SCRIPT
let autoRefreshInterval = null;

function registerServiceWorker() {
  if ("serviceWorker" in navigator) {
    window.addEventListener("load", () => {
      navigator.serviceWorker.register("sw.js")
        .then(reg => {
          console.log("Service Worker registered!", reg.scope);
          // Initial check for updates on load
          checkSWUpdate();
        })
        .catch(err => console.log("Service Worker registration failed: ", err));
    });
  }
}

function checkSWUpdate() {
  if ("serviceWorker" in navigator) {
    navigator.serviceWorker.ready.then((reg) => {
      // Force update check from server
      reg.update().catch(err => console.log("Failed to update SW:", err));
      
      reg.onupdatefound = () => {
        const installingWorker = reg.installing;
        if (installingWorker) {
          installingWorker.onstatechange = () => {
            if (installingWorker.state === "installed") {
              if (navigator.serviceWorker.controller) {
                console.log("New Service Worker version detected!");
                showToast(appState.currentLang === "km" 
                  ? "មានកំណែទម្រង់ថ្មី! កំពុងកែសម្រួលប្រព័ន្ធ..." 
                  : "New version available! Refreshing...");
                setTimeout(() => {
                  window.location.reload();
                }, 2000);
              }
            }
          };
        }
      };
    });
  }
}

function startAutoRefreshTimer() {
  if (autoRefreshInterval) {
    clearInterval(autoRefreshInterval);
  }
  
  // Every 5 minutes (5 * 60 * 1000 ms)
  autoRefreshInterval = setInterval(() => {
    // 1. First trigger Service Worker update check
    checkSWUpdate();
    
    // 2. Refresh local database and view, or reload page if inactive and not editing
    const activeModals = document.querySelectorAll(".modal-overlay.active");
    const activeLogin = document.getElementById("login-overlay") && document.getElementById("login-overlay").style.display === "flex";
    const activeElement = document.activeElement;
    const isTyping = activeElement && (activeElement.tagName === "INPUT" || activeElement.tagName === "TEXTAREA" || activeElement.tagName === "SELECT");

    if (activeModals.length === 0 && !activeLogin && !isTyping) {
      console.log("Auto-refresh: checking inactivity for page reload");
      
      // If user has been inactive for at least 15 seconds, reload the page to fully synchronize code/data
      const timeSinceLastActive = Date.now() - lastActiveTime;
      if (timeSinceLastActive > 15 * 1000) {
        console.log("Forcing page reload for synchronization...");
        window.location.reload();
      } else {
        // Otherwise, just do a silent refresh of the UI and data in background
        initDatabase();
        renderCurrentView();
      }
    }
  }, 5 * 60 * 1000);
}


function exportDatabase() {
  const dataStr = "data:text/json;charset=utf-8," + encodeURIComponent(JSON.stringify(appState));
  const dlAnchorElem = document.createElement("a");
  
  const formattedDate = new Date().toISOString().slice(0,10);
  
  dlAnchorElem.setAttribute("href", dataStr);
  dlAnchorElem.setAttribute("download", `debt_manager_backup_${formattedDate}.json`);
  dlAnchorElem.click();
}

function importDatabase(e) {
  const fileReader = new FileReader();
  fileReader.onload = function(event) {
    try {
      const importedState = JSON.parse(event.target.result);
      
      if (importedState.debtors && importedState.debts && importedState.payments) {
        appState.debtors = importedState.debtors;
        appState.debts = importedState.debts;
        appState.payments = importedState.payments;
        
        saveToLocalStorage();
        showToast(i18n[appState.currentLang].toast_imported);
        renderCurrentView();
      } else {
        alert("Invalid backup file structure!");
      }
    } catch (err) {
      alert("Error parsing backup JSON file!");
    }
  };
  fileReader.readAsText(e.target.files[0]);
}

function resetDatabase() {
  if (confirm(i18n[appState.currentLang].confirm_reset)) {
    localStorage.clear();
    appState.debtors = [];
    appState.debts = [];
    appState.payments = [];
    
    // Fallback reload defaults
    initDatabase();
    showToast(i18n[appState.currentLang].toast_reset_success);
    renderCurrentView();
  }
}

// UI GENERAL HELPERS
function getTodayDateString() {
  const today = new Date();
  const yyyy = today.getFullYear();
  let mm = today.getMonth() + 1; // Months start at 0
  let dd = today.getDate();

  if (dd < 10) dd = "0" + dd;
  if (mm < 10) mm = "0" + mm;

  return `${yyyy}-${mm}-${dd}`;
}

function getCurrentTimeFormatted(lang) {
  const now = new Date();
  const hours = now.getHours();
  const minutes = now.getMinutes().toString().padStart(2, "0");
  
  if (lang === "km") {
    const period = hours >= 12 ? (hours >= 17 ? "ល្ងាច" : "រសៀល") : "ព្រឹក";
    const displayHours = hours % 12 || 12;
    return `ម៉ោង ${displayHours}:${minutes} ${period}`;
  } else {
    const period = hours >= 12 ? "PM" : "AM";
    const displayHours = hours % 12 || 12;
    return `${displayHours}:${minutes} ${period}`;
  }
}

function formatCurrency(amount, currency) {
  if (currency === "USD") {
    return "$" + amount.toFixed(2);
  } else {
    return Math.round(amount).toLocaleString() + " ៛";
  }
}


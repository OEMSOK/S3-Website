// Helper to get 1 month later date
function getMonthLaterDateString(dateStr) {
  const d = new Date(dateStr);
  d.setMonth(d.getMonth() + 1);
  const yyyy = d.getFullYear();
  let mm = d.getMonth() + 1;
  let dd = d.getDate();
  if (dd < 10) dd = "0" + dd;
  if (mm < 10) mm = "0" + mm;
  return `${yyyy}-${mm}-${dd}`;
}


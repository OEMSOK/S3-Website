function saveAggregatePayment(e) {
  e.preventDefault();

  const debtorId = document.getElementById("aggregate-payment-debtor-id").value;
  const currency = document.getElementById("aggregate-payment-currency").value;
  const amount = parseFloat(document.getElementById("aggregate-payment-amount").value);
  const date = document.getElementById("aggregate-payment-date").value;
  const method = document.getElementById("aggregate-payment-method").value;
  const notes = document.getElementById("aggregate-payment-notes").value.trim();

  if (amount <= 0) {
    alert("សូមបញ្ចូលចំនួនទឹកប្រាក់ទូទាត់ឱ្យបានត្រឹមត្រូវ!");
    return;
  }

  // Get active matching currency debts sorted by date (FIFO - oldest first)
  const matchingDebts = appState.debts.filter(d => d.debtorId === debtorId && d.currency === currency && d.remaining > 0);
  matchingDebts.sort((a, b) => new Date(a.date) - new Date(b.date));

  // Verify total debt check
  const totalDebtAvailable = matchingDebts.reduce((sum, d) => sum + d.remaining, 0);
  if (amount > totalDebtAvailable) {
    alert("ទឹកប្រាក់ទូទាត់មិនអាចលើសពីសមតុល្យជំពាក់សរុបឡើយ!");
    return;
  }

  let remainingToDistribute = amount;

  matchingDebts.forEach(d => {
    if (remainingToDistribute <= 0) return;

    const debtIndex = appState.debts.findIndex(debt => debt.id === d.id);
    if (debtIndex === -1) return;

    const currentDebt = appState.debts[debtIndex];
    const canPay = parseFloat(Math.min(currentDebt.remaining, remainingToDistribute).toFixed(4));
    
    // deduct from debt
    const newRemaining = parseFloat((currentDebt.remaining - canPay).toFixed(4));
    appState.debts[debtIndex].remaining = newRemaining;
    appState.debts[debtIndex].status = newRemaining <= 0 ? "paid" : "partial";

    // log payment transaction
    const payId = "pay_" + Date.now() + "_" + Math.random().toString(36).substr(2, 5);
    appState.payments.push({
      id: payId,
      debtId: currentDebt.id,
      debtorId,
      amount: canPay,
      date,
      method,
      notes: notes || (appState.currentLang === "km" ? "សងកាត់បំណុលរួម" : "Aggregate payment")
    });

    remainingToDistribute = parseFloat((remainingToDistribute - canPay).toFixed(4));
  });

  saveToLocalStorage();
  closeModal("modal-aggregate-payment");
  showToast(i18n[appState.currentLang].toast_payment_added);

  // If debtor modal details is open, refresh it
  const detailModal = document.getElementById("modal-debtor-detail");
  if (detailModal && detailModal.classList.contains("active")) {
    renderDebtorDetailSublists(debtorId);
  }

  renderCurrentView();
}

// 5. Delete Repayment
function deletePayment(id) {
  const payment = appState.payments.find(p => p.id === id);
  if (!payment) return;

  // Add amount back to debt remaining balance
  const debtIndex = appState.debts.findIndex(d => d.id === payment.debtId);
  if (debtIndex !== -1) {
    const debt = appState.debts[debtIndex];
    const newRemaining = debt.remaining + payment.amount;
    let status = "partial";
    if (newRemaining >= debt.amount) {
      status = "unpaid";
    }
    appState.debts[debtIndex].remaining = newRemaining;
    appState.debts[debtIndex].status = status;
  }

  // Remove payment
  appState.payments = appState.payments.filter(p => p.id !== id);

  saveToLocalStorage();
  showToast(i18n[appState.currentLang].toast_deleted);
  
  renderCurrentView();
}


let firebaseDbRef = null;
let firebaseConnectedListener = null;
let isWritingToCloud = false;

function initCloudSync() {
  const syncEnabled = localStorage.getItem(STORAGE_KEYS.SYNC_ENABLED) === "true";
  const syncConfigStr = localStorage.getItem(STORAGE_KEYS.SYNC_CONFIG) || "";
  const syncShopId = localStorage.getItem(STORAGE_KEYS.SYNC_SHOP_ID) || "sok_sinet_shop";
  
  const form = document.getElementById("form-sync-config");
  const configTextarea = document.getElementById("sync-json-config");
  const shopIdInput = document.getElementById("sync-shop-id");
  
  if (configTextarea) configTextarea.value = syncConfigStr;
  if (shopIdInput) shopIdInput.value = syncShopId;
  
  if (syncEnabled) {
    if (form) form.style.display = "flex";
    if (syncConfigStr) {
      try {
        const config = JSON.parse(syncConfigStr);
        connectFirebase(config, syncShopId);
      } catch (e) {
        updateSyncStatus("error");
      }
    } else {
      updateSyncStatus("connecting");
    }
  } else {
    if (form) form.style.display = "none";
    updateSyncStatus("disabled");
  }
  
  // Event listeners
  document.getElementById("btn-toggle-sync").addEventListener("click", () => {
    const isCurrentlyEnabled = localStorage.getItem(STORAGE_KEYS.SYNC_ENABLED) === "true";
    const lang = appState.currentLang;
    
    if (isCurrentlyEnabled) {
      localStorage.setItem(STORAGE_KEYS.SYNC_ENABLED, "false");
      if (form) form.style.display = "none";
      disconnectFirebase();
      showToast(i18n[lang].toast_sync_disabled);
    } else {
      localStorage.setItem(STORAGE_KEYS.SYNC_ENABLED, "true");
      if (form) form.style.display = "flex";
      updateSyncStatus("connecting");
      showToast(i18n[lang].toast_sync_enabled);
      
      const configStr = localStorage.getItem(STORAGE_KEYS.SYNC_CONFIG);
      if (configStr) {
        try {
          const config = JSON.parse(configStr);
          connectFirebase(config, localStorage.getItem(STORAGE_KEYS.SYNC_SHOP_ID) || "sok_sinet_shop");
        } catch (e) {
          updateSyncStatus("error");
        }
      }
    }
  });
  
  document.getElementById("form-sync-config").addEventListener("submit", (e) => {
    e.preventDefault();
    const lang = appState.currentLang;
    const rawConfig = document.getElementById("sync-json-config").value.trim();
    const shopId = document.getElementById("sync-shop-id").value.trim() || "sok_sinet_shop";
    
    let parsedConfig = null;
    
    try {
      let jsonString = rawConfig;
      if (jsonString.includes("{") && jsonString.includes("}")) {
        const start = jsonString.indexOf("{");
        const end = jsonString.lastIndexOf("}");
        jsonString = jsonString.substring(start, end + 1);
      }
      
      // Clean up Javascript object literal notation to standard JSON
      let cleanedJson = jsonString
        .replace(/([{,]\s*)([a-zA-Z0-9_]+)\s*:/g, '$1"$2":')
        .replace(/'/g, '"')
        .replace(/,\s*([\]}])/g, '$1');
        
      parsedConfig = JSON.parse(cleanedJson);
    } catch (err) {
      console.error("Config parse error:", err);
      alert(lang === "km" ? "កូដ Firebase Config មិនត្រឹមត្រូវ! សូមប្រាកដថាអ្នកបានចម្លង JSON object ត្រឹមត្រូវ។" : "Invalid Firebase Config! Please make sure it is a valid JSON object.");
      return;
    }
    
    if (!parsedConfig || !parsedConfig.databaseURL) {
      alert(lang === "km" ? "រកមិនឃើញ databaseURL ទេ! Firebase Realtime Database ត្រូវការ databaseURL ដើម្បីដំណើរការ។" : "Missing databaseURL! Firebase Realtime Database needs a databaseURL to operate.");
      return;
    }
    
    localStorage.setItem(STORAGE_KEYS.SYNC_CONFIG, JSON.stringify(parsedConfig, null, 2));
    localStorage.setItem(STORAGE_KEYS.SYNC_SHOP_ID, shopId);
    localStorage.setItem(STORAGE_KEYS.SYNC_ENABLED, "true");
    
    showToast(i18n[lang].toast_sync_saved);
    connectFirebase(parsedConfig, shopId);
  });
  
  document.getElementById("btn-push-to-cloud").addEventListener("click", pushLocalDataToCloud);
}

function disconnectFirebase() {
  if (firebaseDbRef) {
    firebaseDbRef.off();
    firebaseDbRef = null;
  }
  if (firebaseConnectedListener) {
    try {
      firebase.database().ref(".info/connected").off("value", firebaseConnectedListener);
    } catch (e) {}
    firebaseConnectedListener = null;
  }
  try {
    if (firebase.apps.length > 0) {
      firebase.app().delete();
    }
  } catch (e) {
    console.error("Error deleting Firebase app:", e);
  }
  updateSyncStatus("disabled");
}

function connectFirebase(config, shopId) {
  disconnectFirebase();
  updateSyncStatus("connecting");
  
  try {
    if (firebase.apps.length === 0) {
      firebase.initializeApp(config);
    }
    const db = firebase.database();
    
    firebaseConnectedListener = db.ref(".info/connected").on("value", (snap) => {
      if (snap.val() === true) {
        updateSyncStatus("connected");
        startListeningToCloud(db, shopId);
      } else {
        updateSyncStatus("connecting");
      }
    }, (err) => {
      console.error("Connection state listener error:", err);
      updateSyncStatus("error");
    });
    
  } catch (err) {
    console.error("Firebase init error:", err);
    updateSyncStatus("error");
    showToast(appState.currentLang === "km" ? "ការភ្ជាប់ Firebase បានបរាជ័យ!" : "Firebase initialization failed!");
  }
}

function startListeningToCloud(db, shopId) {
  const path = `shops/${shopId}`;
  firebaseDbRef = db.ref(path);
  
  firebaseDbRef.on("value", (snapshot) => {
    if (isWritingToCloud) {
      isWritingToCloud = false;
      return;
    }
    
    const data = snapshot.val();
    if (data && (data.debtors || data.debts || data.payments)) {
      console.log("Syncing database from Cloud:", data);
      
      const localDataStr = JSON.stringify({
        debtors: appState.debtors,
        debts: appState.debts,
        payments: appState.payments
      });
      const cloudDataStr = JSON.stringify({
        debtors: data.debtors || [],
        debts: data.debts || [],
        payments: data.payments || []
      });
      
      if (localDataStr !== cloudDataStr) {
        appState.debtors = data.debtors || [];
        appState.debts = data.debts || [];
        appState.payments = data.payments || [];
        
        localStorage.setItem(STORAGE_KEYS.DEBTORS, JSON.stringify(appState.debtors));
        localStorage.setItem(STORAGE_KEYS.DEBTS, JSON.stringify(appState.debts));
        localStorage.setItem(STORAGE_KEYS.PAYMENTS, JSON.stringify(appState.payments));
        
        const activeModals = document.querySelectorAll(".modal-overlay.active");
        const activeElement = document.activeElement;
        const isTyping = activeElement && (activeElement.tagName === "INPUT" || activeElement.tagName === "TEXTAREA" || activeElement.tagName === "SELECT");
        
        if (activeModals.length === 0 && !isTyping) {
          renderCurrentView();
        }
      }
    } else {
      console.log("Cloud database path is empty or uninitialized.");
    }
  }, (err) => {
    console.error("Cloud database listener error (check security rules):", err);
    updateSyncStatus("error");
  });
}

function syncDataToCloud() {
  const syncEnabled = localStorage.getItem(STORAGE_KEYS.SYNC_ENABLED) === "true";
  if (syncEnabled && firebaseDbRef) {
    isWritingToCloud = true;
    firebaseDbRef.set({
      debtors: appState.debtors,
      debts: appState.debts,
      payments: appState.payments,
      lastUpdated: Date.now()
    }).catch(err => {
      console.error("Sync data to cloud failed:", err);
      isWritingToCloud = false;
    });
  }
}

function pushLocalDataToCloud() {
  const lang = appState.currentLang;
  const msg = i18n[lang].confirm_push_cloud || "Are you sure you want to push local data to Cloud? This will overwrite Cloud data.";
  if (!confirm(msg)) return;
  
  if (firebaseDbRef) {
    updateSyncStatus("connecting");
    firebaseDbRef.set({
      debtors: appState.debtors,
      debts: appState.debts,
      payments: appState.payments,
      lastUpdated: Date.now()
    }).then(() => {
      updateSyncStatus("connected");
      showToast(i18n[lang].toast_push_success);
    }).catch(err => {
      console.error("Push data to cloud failed:", err);
      updateSyncStatus("error");
      showToast(lang === "km" ? "ការរុញទិន្នន័យបានបរាជ័យ!" : "Failed to push data!");
    });
  } else {
    showToast(lang === "km" ? "សូមភ្ជាប់ទៅកាន់ Database ជាមុនសិន!" : "Please connect to Database first!");
  }
}

function updateSyncStatus(status) {
  const dot = document.getElementById("sync-status-dot");
  const text = document.getElementById("sync-status-text");
  const btnToggle = document.getElementById("btn-toggle-sync");
  const lang = appState.currentLang;
  
  if (!dot || !text || !btnToggle) return;
  
  dot.className = "";
  dot.style.backgroundColor = "";
  
  if (status === "connected") {
    dot.classList.add("sync-status-dot-connected");
    text.textContent = i18n[lang].sync_status_connected || "Connected (Realtime)";
    btnToggle.textContent = i18n[lang].btn_sync_disable || "Disable Sync";
    btnToggle.className = "btn btn-danger btn-sm";
  } else if (status === "connecting") {
    dot.classList.add("sync-status-dot-connecting");
    text.textContent = i18n[lang].sync_status_connecting || "Connecting...";
    btnToggle.textContent = i18n[lang].btn_sync_disable || "Disable Sync";
    btnToggle.className = "btn btn-danger btn-sm";
  } else if (status === "error") {
    dot.classList.add("sync-status-dot-error");
    text.textContent = i18n[lang].sync_status_error || "Connection Error";
    btnToggle.textContent = i18n[lang].btn_sync_disable || "Disable Sync";
    btnToggle.className = "btn btn-danger btn-sm";
  } else {
    dot.classList.add("sync-status-dot-disabled");
    text.textContent = i18n[lang].sync_status_disabled || "Disconnected";
    btnToggle.textContent = i18n[lang].btn_sync_enable || "Enable Sync";
    btnToggle.className = "btn btn-outline btn-sm";
  }
}

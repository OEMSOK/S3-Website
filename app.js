// 2. Save Debt Record
function saveDebt(e) {
  e.preventDefault();

  const idInput = document.getElementById("debt-form-id").value;
  const typedName = document.getElementById("debt-debtor-name").value.trim();
  const typedPhone = document.getElementById("debt-debtor-phone").value.trim();
  const selectedVillage = document.getElementById("debt-village").value;
  const date = document.getElementById("debt-date").value;
  const dueDate = document.getElementById("debt-due-date").value || null;

  if (!typedName || !selectedVillage) {
    alert("សូមបញ្ចូលឈ្មោះអ្នកជំពាក់ និងជ្រើសរើសភូមិ!");
    return;
  }

  // 1. Resolve or create Debtor
  let debtor = appState.debtors.find(d => d.name.toLowerCase() === typedName.toLowerCase());
  if (!debtor) {
    const newDebtorId = "debtor_" + Date.now();
    debtor = { id: newDebtorId, name: typedName, phone: typedPhone, address: selectedVillage };
    appState.debtors.push(debtor);
  } else {
    debtor.phone = typedPhone;
    debtor.address = selectedVillage;
  }

  // 2. Extract items from rows with JS Validation
  const items = [];
  const rows = document.querySelectorAll(".debt-item-row");
  let validationError = false;

  rows.forEach((row, idx) => {
    if (validationError) return;

    const select = row.querySelector(".item-select");
    const customText = row.querySelector(".item-custom-text");
    const priceInp = row.querySelector(".item-price");
    const qtyInp = row.querySelector(".item-qty");

    const hasItemSelect = select && select.value !== "";
    const hasPrice = priceInp && priceInp.value.trim() !== "";
    const hasQty = qtyInp && qtyInp.value.trim() !== "";

    // If they filled out any field in a row, they must fill out the complete row
    if (hasItemSelect || hasPrice || hasQty) {
      if (!hasItemSelect) {
        alert(`សូមជ្រើសរើសប្រភេទអីវ៉ាន់នៅជួរទី ${idx + 1}!`);
        select.focus();
        validationError = true;
        return;
      }
      
      const name = select.value === "Other" ? customText.value.trim() : select.value;
      if (select.value === "Other" && !name) {
        alert(`សូមបញ្ចូលឈ្មោះទំនិញផ្សេងៗនៅជួរទី ${idx + 1}!`);
        customText.focus();
        validationError = true;
        return;
      }

      const price = parseFloat(priceInp.value);
      if (isNaN(price) || price < 0) {
        alert(`សូមបញ្ចូលតម្លៃរាយឱ្យបានត្រឹមត្រូវនៅជួរទី ${idx + 1}!`);
        priceInp.focus();
        validationError = true;
        return;
      }

      const qty = parseFloat(qtyInp.value);
      if (isNaN(qty) || qty <= 0) {
        alert(`សូមបញ្ចូលចំនួនឱ្យបានត្រឹមត្រូវនៅជួរទី ${idx + 1}!`);
        qtyInp.focus();
        validationError = true;
        return;
      }

      items.push({ name, price, qty });
    }
  });

  if (validationError) return;

  if (items.length === 0) {
    alert("សូមបំពេញទិន្នន័យទំនិញជំពាក់ឱ្យបានយ៉ាងហោចណាស់ ១ជួរ!");
    return;
  }

  // Calculate total sum
  const amount = items.reduce((sum, item) => sum + (item.price * item.qty), 0);
  const displayItemStr = items.map(it => `${it.name} (x${it.qty})`).join(", ");

  if (idInput) {
    // Edit existing debt
    const index = appState.debts.findIndex(d => d.id === idInput);
    if (index !== -1) {
      const prevDebt = appState.debts[index];
      let totalPaid = prevDebt.amount - prevDebt.remaining;
      let newRemaining = amount - totalPaid;
      let newStatus = "unpaid";
      
      if (newRemaining <= 0) {
        newRemaining = 0;
        newStatus = "paid";
      } else if (newRemaining < amount) {
        newStatus = "partial";
      }

      appState.debts[index] = {
        ...prevDebt,
        debtorId: debtor.id,
        village: selectedVillage,
        items,
        item: displayItemStr,
        amount,
        remaining: newRemaining,
        status: newStatus,
        date,
        dueDate
      };
    }
  } else {
    // Create new debt
    const newId = "debt_" + Date.now();
    appState.debts.push({
      id: newId,
      debtorId: debtor.id,
      village: selectedVillage,
      items,
      item: displayItemStr,
      amount,
      currency: "KHR",
      date,
      dueDate,
      remaining: amount,
      status: "unpaid",
      notes: ""
    });
  }

  saveToLocalStorage();
  closeModal("modal-debt");
  showToast(i18n[appState.currentLang].toast_saved);
  
  renderCurrentView();
}


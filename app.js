// DYNAMIC DASHBOARD RENDERER
function renderDashboard() {
  // 1. Calculate balances
  let unpaidUSD = 0;
  let unpaidKHR = 0;
  let paidUSD = 0;
  let paidKHR = 0;
  let activeDebtorsSet = new Set();

  appState.debts.forEach(d => {
    const isUnpaid = d.status === "unpaid" || d.status === "partial";
    const isPaid = d.status === "paid";
    
    // Unpaid/Remaining calculations
    if (d.currency === "USD") {
      unpaidUSD += d.remaining;
      paidUSD += (d.amount - d.remaining);
    } else {
      unpaidKHR += d.remaining;
      paidKHR += (d.amount - d.remaining);
    }

    if (d.remaining > 0) {
      activeDebtorsSet.add(d.debtorId);
    }
  });

  // Write stats values
  document.getElementById("stat-unpaid-usd").textContent = formatCurrency(unpaidUSD, "USD");
  document.getElementById("stat-unpaid-khr").textContent = formatCurrency(unpaidKHR, "KHR");
  document.getElementById("stat-paid-usd").textContent = formatCurrency(paidUSD, "USD");
  document.getElementById("stat-paid-khr").textContent = formatCurrency(paidKHR, "KHR");
  document.getElementById("stat-debtors-count").textContent = activeDebtorsSet.size;

  // 2. Render Activity list (5 most recent activities)
  renderRecentActivities();

  // 3. Render Custom SVG Monthly Chart
  renderSVGChart();
}

function renderRecentActivities() {
  const container = document.getElementById("recent-activity-list");
  container.innerHTML = "";

  // Combine debts and payments into activities sorted by date desc
  let activities = [];

  appState.debts.forEach(d => {
    const debtor = appState.debtors.find(dr => dr.id === d.debtorId);
    activities.push({
      type: "debt",
      title: debtor ? debtor.name : "Unknown Customer",
      subtitle: d.item,
      amount: d.amount,
      currency: d.currency,
      date: d.date,
      rawDate: new Date(d.date)
    });
  });

  appState.payments.forEach(p => {
    const debtor = appState.debtors.find(dr => dr.id === p.debtorId);
    const debt = appState.debts.find(d => d.id === p.debtId);
    activities.push({
      type: "payment",
      title: debtor ? debtor.name : "Unknown Customer",
      subtitle: debt ? `${i18n[appState.currentLang].action_pay} - ${debt.item}` : i18n[appState.currentLang].action_pay,
      amount: p.amount,
      currency: debt ? debt.currency : "USD",
      date: p.date,
      rawDate: new Date(p.date)
    });
  });

  // Sort: most recent first
  activities.sort((a, b) => b.rawDate - a.rawDate);
  const recent = activities.slice(0, 5);

  if (recent.length === 0) {
    container.innerHTML = `<div class="empty-state"><span>${i18n[appState.currentLang].no_activities}</span></div>`;
    return;
  }

  recent.forEach(act => {
    const item = document.createElement("div");
    item.className = `activity-item type-${act.type}`;
    
    const formattedAmount = formatCurrency(act.amount, act.currency);
    const amtClass = act.type === "debt" ? "debt" : "payment";
    const amtSymbol = act.type === "debt" ? "+" : "-";

    item.innerHTML = `
      <div class="act-desc">
        <span class="act-title">${act.title}</span>
        <span class="act-subtitle">${act.subtitle}</span>
        <span class="act-subtitle" style="font-family: var(--font-numbers);">${act.date}</span>
      </div>
      <div class="act-amount ${amtClass}">
        ${amtSymbol}${formattedAmount}
      </div>
    `;
    container.appendChild(item);
  });
}

